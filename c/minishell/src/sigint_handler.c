#include "minishell.h"

/*	signal_handler.c: 1.	*/
/*	CTRL-D = EOF (technically not a signal)
	CTRL-C = SIGINT
	CTRL-\ = SIGQUIT							*/

void	sigint_handler(int sig)
{
	signal(sig, SIG_IGN);
	if (sig == SIGINT)
	{
		write(STDOUT_FILENO, "\n", 1);
		rl_on_new_line();
		rl_replace_line("", 0);
		rl_redisplay();
		signal(SIGINT, sigint_handler);
	}
}
